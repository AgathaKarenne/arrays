public class exercicio2 {
    //Crie um array de strings contendo nomes de cores (por exemplo, "vermelho", "verde", "azul"). Usando um loop for, percorra o array e exiba cada cor.

    public static void main (String[] args) {

        // crie um array de string com nomes das cores
        String[] cores = {"vermelho","verde", "azul", "amarelo"};

        //usando um loop for para pecorrer o array e exibir a cor
        for(int i = 0; i < cores.length; i++){
            System.out.println(cores[i]);
        }
    }


}


